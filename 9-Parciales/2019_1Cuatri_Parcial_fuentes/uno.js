
function mostrar()
{
    let lado1;
    let lado2;
    let lado3;
    let perimetro;

    lado1 = prompt("Ingrese el primer lado del Triangulo");
    lado2 = prompt("Ingrese el segundo lado del Triangulo");
    lado3 = prompt("Ingrese el tercer lado del Triangulo");

    lado1 = parseInt(lado1);
    lado2 = parseInt(lado2);
    lado3 = parseInt(lado3);

    perimetro = lado1 + lado2 + lado3;

    alert("El perimetro del Triangulo Equilatero es de : " + perimetro);





}
